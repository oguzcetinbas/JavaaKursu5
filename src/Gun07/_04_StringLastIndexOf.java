package Gun07;

public class _04_StringLastIndexOf {

    public static void main(String[] args) {

        // indexOf gibi çalışır.Bu aramaya baştan başlar
        // LastIndexOf ise : aramaya sondan başlar.


        //         0123456
        String s1="Merhaba";

        System.out.println("s1.indexOf(a) = " + s1.indexOf("a")); // 4 index verir.
        System.out.println("s1.lastIndexOf(a) = " + s1.lastIndexOf("a")); // 6 index değişmez aramaya başladığı yer değişir.







    }
}
